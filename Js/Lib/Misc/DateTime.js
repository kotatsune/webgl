'use strict';


var DateTime = {};


//--------------------------------------------------------------------------------
// 現在の時刻（ミリ秒）を取得します。
//--------------------------------------------------------------------------------
DateTime.now = function ()
{
	if ( window.performance.now )
	{
		return window.performance.now();
	}
	else
	{
		return Date.now();
	}
};
