'use strict';


//--------------------------------------------------------------------------------
// コンストラクタ
//--------------------------------------------------------------------------------
function Scene ( name )
{
	this.name = name;
	this.active = false;
}


//--------------------------------------------------------------------------------
// シーン名を取得します。
//--------------------------------------------------------------------------------
Scene.prototype.getName = function ()
{
	return this.name;
};


//--------------------------------------------------------------------------------
// シーンがアクティブかどうかを判定します。
//--------------------------------------------------------------------------------
Scene.prototype.isActive = function ()
{
	return this.active;
};


//--------------------------------------------------------------------------------
// シーンをアクティブ化/非アクティブ化します。
//--------------------------------------------------------------------------------
Scene.prototype.activate = function ( active )
{
	this.active = active;
};


