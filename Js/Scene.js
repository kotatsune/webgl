// シーンが開始されるときに呼ばれます。
//--------------------------------------------------------------------------------
Scene.prototype.onResume = function ( core, gl )
{
};


//--------------------------------------------------------------------------------
// シーンが停止されるときに呼ばれます。
//--------------------------------------------------------------------------------
Scene.prototype.onPause = function ( core, gl )
{
};


//--------------------------------------------------------------------------------
// シーンが更新されるときに呼ばれます。
//--------------------------------------------------------------------------------
Scene.prototype.onUpdate = function ( core, gl )
{
};


//--------------------------------------------------------------------------------
// シーンが描画されるときに呼ばれます。
//--------------------------------------------------------------------------------
Scene.prototype.onRender = function ( core, gl )
{
};

